def get_coordinates(address, api_key):
    base_url = "https://maps.googleapis.com/maps/api/geocode/json"
    params = {
        "address": address,
        "key": api_key
    }
    try:
        response = requests.get(base_url, params=params, timeout=10)
        response.raise_for_status()
        data = response.json()

        if data["status"] == "OK" and data["results"]:
            location = data["results"][0]["geometry"]["location"]
            return location["lat"], location["lng"]
        else:
            print(f"Error getting coordinates for {address}: {data['status']}")
            return None, None
    except requests.exceptions.RequestException as e:
        print(f"Network error for {address}: {e}")
        return None, None

def search_places(query, location, radius, api_key):
    all_places = []
    next_page_token = None
    page_count = 0

    while True:
        url = (
            "https://maps.googleapis.com/maps/api/place/textsearch/json"
            f"?query={query}&location={location}&radius={radius}&key={api_key}"
        )
        if next_page_token:
            url += f"&pagetoken={next_page_token}"

        try:
            r = requests.get(url, timeout=30)
            r.raise_for_status()
            data = r.json()

            all_places.extend(data.get("results", []))
            next_page_token = data.get("next_page_token")
            page_count += 1

            if not next_page_token:
                break

            time.sleep(2)
        except requests.exceptions.RequestException as e:
            print(f"Network error during place search for '{query}': {e}")
            break
    return all_places

def place_details(place_id, api_key):
    url = (
        "https://maps.googleapis.com/maps/api/place/details/json"
        f"?place_id={place_id}&key={api_key}"
    )
    try:
        r = requests.get(url, timeout=30)
        r.raise_for_status()
        return r.json().get("result", {})
    except requests.exceptions.RequestException as e:
        print(f"Network error getting details for place_id {place_id}: {e}")
        return {}

def infer_type_from_query(query_text: str) -> str:
    q = query_text.lower()
    for k, v in TYPE_MAP.items():
        if k in q:
            return v
    return ""

